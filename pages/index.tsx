import type { NextPage } from 'next'
import Head from 'next/head'
import styles from '../styles/Home.module.css'
import  useSWR, { Fetcher } from 'swr'
import Blog from '../models/blog'
import Button, {Elevation} from '../components/button'
import { Colors } from '../constants'

const Home: NextPage = () => {

  const fetcher: Fetcher<Blog[]> = (url: string) => fetch(url).then(res => res.json());

  const {data} = useSWR<Blog[]>('api/blogs', fetcher)

  if (!data)
  {
    return <p>Loading...</p>
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>
          Welcome to <span>Author.io</span>
        </h1>
        <Button color={Colors.Primary} elevation={ Elevation.dp0 }>Get Started</Button>
      </main>
    </div>
  )
}

export default Home
